// Grand échange « éditeur » observé sur HX Edit entre subscribe f0 et le polling régulier
// (`connect_device_30s_HXEdit.json` : 19 ed ×3 puis 1a ef). Sans cela le Stomp peut ignorer
// les polls `08…f0:03` (captures Linux).

use crate::helix::HelixState;
use crate::helix::packet::OutPacket;

const INTER_PACKET_DELAY_MS: u64 = 20;

/// Envoie les requêtes longues `ed` / `ef` qui amorcent l’état preset + liste (phase 4).
pub fn send(state: &mut HelixState) {
    let c0 = state.next_x80_cnt();
    state.send(OutPacket::with_delay(
        vec![
            0x19, 0x00, 0x00, 0x18,
            0x80, 0x10, 0xed, 0x03,
            0x00, c0, 0x00, 0x04,
            0x09, 0x10, 0x00, 0x00,
            0x01, 0x00, 0x06, 0x00,
            0x09, 0x00, 0x00, 0x00,
            0x83, 0x66, 0xcd, 0x03,
            0xe8, 0x64, 0x4c, 0x65,
            0x80, 0x00, 0x00, 0x00,
        ],
        INTER_PACKET_DELAY_MS,
    ));

    let c1 = state.next_x80_cnt();
    state.send(OutPacket::with_delay(
        vec![
            0x19, 0x00, 0x00, 0x18,
            0x80, 0x10, 0xed, 0x03,
            0x00, c1, 0x00, 0x0c,
            0x6c, 0x10, 0x00, 0x00,
            0x01, 0x00, 0x06, 0x00,
            0x09, 0x00, 0x00, 0x00,
            0x83, 0x66, 0xcd, 0x03,
            0xea, 0x64, 0x17, 0x65,
            0xc0, 0x00, 0x00, 0x00,
        ],
        INTER_PACKET_DELAY_MS,
    ));

    let c2 = state.next_x80_cnt();
    state.send(OutPacket::with_delay(
        vec![
            0x19, 0x00, 0x00, 0x18,
            0x80, 0x10, 0xed, 0x03,
            0x00, c2, 0x00, 0x0c,
            0x9d, 0x10, 0x00, 0x00,
            0x01, 0x00, 0x06, 0x00,
            0x09, 0x00, 0x00, 0x00,
            0x83, 0x66, 0xcd, 0x03,
            0xeb, 0x64, 0x16, 0x65,
            0xc0, 0x00, 0x00, 0x00,
        ],
        INTER_PACKET_DELAY_MS,
    ));

    let cx = state.next_x1_cnt();
    state.send(OutPacket::with_delay(
        vec![
            0x1a, 0x00, 0x00, 0x18,
            0x01, 0x10, 0xef, 0x03,
            0x00, cx, 0x00, 0x04,
            0x09, 0x10, 0x00, 0x00,
            0x01, 0x00, 0x02, 0x00,
            0x0a, 0x00, 0x00, 0x00,
            0x83, 0x66, 0xcd, 0x03,
            0xe8, 0x64, 0xcc, 0xfe,
            0x65, 0x80, 0x00, 0x00,
        ],
        INTER_PACKET_DELAY_MS,
    ));
}
